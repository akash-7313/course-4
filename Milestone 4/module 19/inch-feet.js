function inchToFeet(inches) {
    var feet = inches / 12;
    return feet;
}
var myInches = 120;
var inFeet = inchToFeet(myInches);
console.log('my inches to feet: ', inFeet);

var vaiInches = 144;
var inFeet = inchToFeet(vaiInches);
console.log('vai inches to feet: ', inFeet);

var bonInches = 168;
var inFeet = inchToFeet(bonInches);
console.log('bon inches to feet: ', inFeet);















